package ch2;

public class SpecialCharEx {
    public static void main(String[] args) {
        // \t = tab \b = backslash
        System.out.println('\'');
        System.out.println("abc\t123\b456");
        System.out.println('\n');
        System.out.println("\"Hello\"");
        System.out.println("c:\\");
    }
}
